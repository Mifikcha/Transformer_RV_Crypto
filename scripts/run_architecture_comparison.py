"""
Unified architecture comparison for RV regression.

Fair-comparison features:
- Explicit GPU control (--device auto|cuda|cpu); cuDNN benchmark + TF32 on GPU.
- Multi-seed evaluation (--seeds / --n-seeds) with mean +- std aggregation.
- Clean train/val/test split within each fold via val_frac (--val-frac).
  Inner val is used for early stopping; the original fold-val becomes a
  held-out TEST set used only for reported metrics.
- Matched-param mode (--match-params) that binary-searches d_model per
  architecture so that all architectures have comparable parameter budgets.
- Lightweight per-architecture random-search HPO (--hpo-trials) over
  {lr, dropout, weight_decay}, selected by mean inner-val loss (no leakage).
- Feature-set selection (--feature-set recommended|full) to surface the
  bias coming from a feature list generated by a non-transformer model.
- Cost/throughput measurement per architecture (latency, peak memory).
- Baselines (persistence / historical_mean / linear / lightgbm / lstm) are
  evaluated on the same folds and merged into the same CSV.

Outputs (under transformer/output/experiments/):
- architecture_comparison.csv           : one row per model_type with
  mean/std metrics and cost columns.
- architecture_comparison.signature.json: hash of run configuration.
- architecture_hpo_trials.csv           : per-trial HPO log (when enabled).
"""

