def _enable_gpu_perf_flags(torch_module) -> None:
    if not torch_module.cuda.is_available():
        return
    torch_module.backends.cudnn.benchmark = True
    try:
        torch_module.backends.cuda.matmul.allow_tf32 = True
        torch_module.backends.cudnn.allow_tf32 = True
    except Exception:
        pass


def _resolve_seeds(args: argparse.Namespace, base_seed: int) -> list[int]:
    if args.seeds:
        seeds = [int(s.strip()) for s in args.seeds.split(",") if s.strip()]
        if not seeds:
            raise ValueError("--seeds is empty after parsing.")
        return seeds
    if args.n_seeds is not None and args.n_seeds > 0:
        return [int(base_seed) + i for i in range(args.n_seeds)]
    return [int(base_seed)]


def _config_signature(payload: dict) -> str:
    encoded = json.dumps(payload, sort_keys=True, ensure_ascii=False, default=str).encode("utf-8")
    return hashlib.sha256(encoded).hexdigest()[:16]


def _progress(step: int, total: int, message: str) -> None:
    pct = int((step / max(total, 1)) * 100)
    now = time.strftime("%H:%M:%S")
    print(f"[{now}] [{step}/{total}] ({pct:>3}%) {message}")


def _aggregate_metric(per_run_metrics: list[dict], key: str) -> tuple[float, float]:
    import numpy as np
    vals = [float(m[key]) for m in per_run_metrics if key in m and m[key] is not None]
    vals = [v for v in vals if np.isfinite(v)]
    if not vals:
        return float("nan"), float("nan")
    mean = float(np.mean(vals))
    std = float(np.std(vals, ddof=1)) if len(vals) > 1 else 0.0
    return mean, std


def _count_model_params(
    model_type: str,
    input_dim: int,
    seq_len: int,
    patch_size: int,
    d_model: int,
    n_heads: int,
    n_enc_layers: int,
    n_dec_layers: int,
    d_ff: int,
    n_horizons: int,
    dropout: float,
) -> int:
    from transformer.model import build_model
    m = build_model(
        model_type=model_type,
        input_dim=input_dim,
        seq_len=seq_len,
        patch_size=patch_size,
        d_model=d_model,
        n_heads=n_heads,
        n_layers=n_enc_layers,
        d_ff=d_ff,
        dropout=dropout,
        n_horizons=n_horizons,
        n_enc_layers=n_enc_layers,
        n_dec_layers=n_dec_layers,
    )
    n = sum(p.numel() for p in m.parameters())
    del m
    return int(n)


def _find_dmodel_for_target_params(
    target_params: int,
    *,
    model_type: str,
    input_dim: int,
    seq_len: int,
    patch_size: int,
    n_heads: int,
    n_enc_layers: int,
    n_dec_layers: int,
    n_horizons: int,
    dropout: float,
    d_model_min: int = 16,
    d_model_max: int = 512,
) -> tuple[int, int]:
    """Return (best_d_model, param_count) minimizing |params - target|."""
    best_d = d_model_min
    best_diff = float("inf")
    best_n = 0
    # d_model must be divisible by n_heads; step by n_heads.
    prev_n = -1
    increased = False
    for d in range(max(d_model_min, n_heads), d_model_max + 1, n_heads):
        d_ff = 4 * d
        n = _count_model_params(
            model_type=model_type,
            input_dim=input_dim,
            seq_len=seq_len,
            patch_size=patch_size,
            d_model=d,
            n_heads=n_heads,
            n_enc_layers=n_enc_layers,
            n_dec_layers=n_dec_layers,
            d_ff=d_ff,
            n_horizons=n_horizons,
            dropout=dropout,
        )
        diff = abs(n - target_params)
        if diff < best_diff:
            best_diff = diff
            best_d = d
            best_n = n
        # Near-monotonic: once we overshoot target by a lot, stop.
        if prev_n >= 0 and n > target_params:
            increased = True
        prev_n = n
        if increased and n > 2 * target_params:
            break
    return best_d, best_n


def _measure_cost(
    *,
    model_type: str,
    device,
    input_dim: int,
    seq_len: int,
    patch_size: int,
    d_model: int,
    n_heads: int,
    n_enc_layers: int,
    n_dec_layers: int,
    d_ff: int,
    n_horizons: int,
    dropout: float,
    batch_size: int = 64,
    n_iter: int = 50,
) -> dict:
    import torch
    from transformer.dataset import N_HAR_CONTEXT
    from transformer.model import build_model

    use_har = str(model_type).lower() in {"patch_encoder", "encoder_only"}
    model = build_model(
        model_type=model_type,
        input_dim=input_dim,
        seq_len=seq_len,
        patch_size=patch_size,
        d_model=d_model,
        n_heads=n_heads,
        n_layers=n_enc_layers,
        d_ff=d_ff,
        dropout=dropout,
        n_horizons=n_horizons,
        n_enc_layers=n_enc_layers,
        n_dec_layers=n_dec_layers,
    ).to(device).eval()

    x = torch.randn(batch_size, seq_len, input_dim, device=device)
    har = torch.randn(batch_size, N_HAR_CONTEXT, device=device) if use_har else None

    # Warm-up
    with torch.no_grad():
        for _ in range(5):
            _ = model(x, har) if use_har else model(x)
    if device.type == "cuda":
        torch.cuda.synchronize()
        torch.cuda.reset_peak_memory_stats()

    t0 = time.perf_counter()
    with torch.no_grad():
        for _ in range(n_iter):
            _ = model(x, har) if use_har else model(x)
    if device.type == "cuda":
        torch.cuda.synchronize()
    dt = time.perf_counter() - t0
    latency_ms_batch = (dt / n_iter) * 1000.0
    throughput = (n_iter * batch_size) / dt if dt > 0 else float("nan")
    peak_mb = (
        torch.cuda.max_memory_allocated() / (1024 * 1024)
        if device.type == "cuda"
        else float("nan")
    )
    del model
    if device.type == "cuda":
        torch.cuda.empty_cache()
    return {
        "latency_ms_batch": float(latency_ms_batch),
        "latency_ms_sample": float(latency_ms_batch / max(batch_size, 1)),
        "throughput_samples_per_sec": float(throughput),
        "peak_mem_mb": float(peak_mb),
        "cost_batch_size": int(batch_size),
    }


def _hpo_random_search(
    *,
    args: argparse.Namespace,
    model_type: str,
    model_cfg,
    train_cfg,
    data_path: str,
    feature_columns: list[str],
    val_frac: float,
    seed: int,
    n_trials: int,
) -> tuple[dict, list[dict]]:
    """Return (best_hparams, trial_logs). Select by mean inner-val loss."""
    import random
    from dataclasses import replace
    from transformer.train import train_walk_forward_regression

    hpo_epochs = max(3, int(round(train_cfg.max_epochs * float(args.hpo_epoch_frac))))
    rng = random.Random(int(seed))
    trial_logs: list[dict] = []
    best_hp: dict | None = None
    best_loss = float("inf")
    for t in range(n_trials):
        lr = 10 ** rng.uniform(-5, -3)
        dropout = rng.choice([0.1, 0.15, 0.2, 0.25, 0.3])
        weight_decay = 10 ** rng.uniform(-6, -3)
        m_cfg = replace(model_cfg, dropout=float(dropout))
        t_cfg = replace(
            train_cfg,
            lr=float(lr),
            weight_decay=float(weight_decay),
            max_epochs=int(hpo_epochs),
            seed=int(seed),
        )
        started = time.perf_counter()
        try:
            result = train_walk_forward_regression(
                data_path=data_path,
                features_path="",
                model_cfg=m_cfg,
                train_cfg=t_cfg,
                save_models=False,
                model_prefix=f"hpo_{model_type}_t{t}",
                run_name=f"hpo_{model_type}_t{t}",
                val_frac=val_frac,
                feature_columns=feature_columns,
            )
        except Exception as exc:
            trial_logs.append({
                "model_type": model_type, "trial": t, "lr": lr,
                "dropout": float(dropout), "weight_decay": weight_decay,
                "mean_val_loss": float("inf"), "elapsed_sec": 0.0, "error": str(exc),
            })
            continue
        elapsed = time.perf_counter() - started
        fold_val_losses = [float(m.get("val_loss", float("inf"))) for m in result["metrics_per_fold"]]
        mean_vl = float(sum(fold_val_losses) / max(len(fold_val_losses), 1)) if fold_val_losses else float("inf")
        trial_logs.append({
            "model_type": model_type, "trial": t, "lr": float(lr),
            "dropout": float(dropout), "weight_decay": float(weight_decay),
            "mean_val_loss": mean_vl, "elapsed_sec": round(elapsed, 2),
        })
        if mean_vl < best_loss:
            best_loss = mean_vl
            best_hp = {"lr": float(lr), "dropout": float(dropout), "weight_decay": float(weight_decay)}
        print(
            f"  [HPO {model_type} t={t+1}/{n_trials}] "
            f"lr={lr:.2e} do={dropout:.2f} wd={weight_decay:.2e} "
            f"mean_val_loss={mean_vl:.6f} elapsed={elapsed:.1f}s"
        )
    if best_hp is None:
        best_hp = {"lr": train_cfg.lr, "dropout": model_cfg.dropout, "weight_decay": train_cfg.weight_decay}
    return best_hp, trial_logs


def _run_baselines_block(
    *,
    data_path: str,
    n_splits: int,
    target_columns: tuple[str, ...],
    metric_keys: tuple[str, ...],
    project_root: str,
) -> list[dict]:
    import numpy as np

    baselines_dir = os.path.join(project_root, "baselines")
    if baselines_dir not in sys.path:
        sys.path.insert(0, baselines_dir)

    from historical_mean_baseline import run as run_hist  # type: ignore
    from har_rv_baseline import run as run_har_rv  # type: ignore
    from har_rv_j_baseline import run as run_har_rv_j  # type: ignore
    from lightgbm_baseline import run as run_lgbm  # type: ignore
    from linear_regression_baseline import run as run_lin  # type: ignore
    from lstm_baseline import run as run_lstm  # type: ignore
    from persistence_baseline import run as run_pers  # type: ignore

    specs = [
        ("baseline:persistence", run_pers),
        ("baseline:historical_mean", run_hist),
        ("baseline:har_rv", run_har_rv),
        ("baseline:har_rv_j", run_har_rv_j),
        ("baseline:linear_ridge", run_lin),
        ("baseline:lightgbm", run_lgbm),
    ]
    if os.environ.get("SKIP_LSTM_BASELINE", "").strip() not in {"1", "true", "True"}:
        specs.append(("baseline:lstm", run_lstm))
    rows: list[dict] = []
    for name, fn in specs:
        print("\n" + "-" * 90)
        print(f"[BASELINE] {name} | folds={n_splits}")
        print("-" * 90)
        t0 = time.perf_counter()
        try:
            out = fn(
                data_path=data_path,
                n_splits=n_splits,
                target_columns=target_columns,
                return_predictions=False,
            )
        except Exception as exc:
            elapsed = time.perf_counter() - t0
            print(f"  [BASELINE] {name} failed: {exc}")
            row: dict = {
                "model_type": name, "n_params": float("nan"),
                "n_seeds": 1, "n_folds": n_splits, "n_samples": 0,
                "device": "cpu", "elapsed_sec": round(elapsed, 2),
                "latency_ms_batch": float("nan"), "latency_ms_sample": float("nan"),
                "throughput_samples_per_sec": float("nan"), "peak_mem_mb": float("nan"),
                "cost_batch_size": float("nan"), "error": str(exc),
            }
            for k in metric_keys:
                row[k] = float("nan")
                row[k.replace("_mean", "_std")] = float("nan")
            row["model_config"] = ""
            rows.append(row)
            continue
        # Backward-compatible: baselines may return list[dict] or a dict bundle.
        if isinstance(out, dict) and "metrics_per_fold" in out:
            fold_metrics = out["metrics_per_fold"]
        else:
            fold_metrics = out
        elapsed = time.perf_counter() - t0
        row = {
            "model_type": name, "n_params": float("nan"),
            "n_seeds": 1, "n_folds": n_splits, "n_samples": len(fold_metrics),
            "device": "cpu", "elapsed_sec": round(elapsed, 2),
            "latency_ms_batch": float("nan"), "latency_ms_sample": float("nan"),
            "throughput_samples_per_sec": float("nan"), "peak_mem_mb": float("nan"),
            "cost_batch_size": float("nan"),
        }
        for k in metric_keys:
            mean, std = _aggregate_metric(fold_metrics, k)
            row[k] = mean
            row[k.replace("_mean", "_std")] = std
        row["model_config"] = ""
        rows.append(row)
        print(
            f"[BASELINE DONE] {name}: mse={row['mse_mean']:.6f} "
            f"qlike={row['qlike_mean']:.6f} r2={row['r2_mean']:.4f} elapsed={row['elapsed_sec']}s"
        )
    return rows


def _extract_loss_streams(pred_df: pd.DataFrame, target_columns: tuple[str, ...]) -> dict[str, np.ndarray]:
    """Return per-horizon and aggregate loss streams from a pred_df."""
    from scripts.stats_tests import qlike_series  # local import to avoid cycles

    out: dict[str, np.ndarray] = {}
    loss_parts: list[np.ndarray] = []
    for col in target_columns:
        a = pred_df[f"actual_{col}"].to_numpy(dtype=float)
        p = pred_df[f"pred_{col}"].to_numpy(dtype=float)
        ls = qlike_series(a, p)
        out[col] = ls
        loss_parts.append(ls.reshape(-1, 1))
    if loss_parts:
        out["agg"] = np.mean(np.concatenate(loss_parts, axis=1), axis=1)
    else:
        out["agg"] = np.array([], dtype=float)
    return out


def _compute_dm_and_bootstrap(
    *,
    pred_m0: pd.DataFrame,
    pred_m1: pd.DataFrame,
    target_columns: tuple[str, ...],
    block_size: int,
    n_boot: int,
    hac_lags: int | None,
    seed: int = 42,
) -> dict[str, float]:
    """Compute DM p-values and bootstrap CI for mean(loss_m0 - loss_m1)."""
    from scripts.stats_tests import dm_test, moving_block_bootstrap_mean_ci

    # Align on timestamps to be safe.
    if "ts" in pred_m0.columns and "ts" in pred_m1.columns:
        a = pred_m0.copy()
        b = pred_m1.copy()
        a["ts"] = pd.to_datetime(a["ts"], utc=True, errors="coerce")
        b["ts"] = pd.to_datetime(b["ts"], utc=True, errors="coerce")
        merged = a.merge(b, on="ts", how="inner", suffixes=("_m0", "_m1"))
        # Rebuild aligned frames.
        pred0 = pd.DataFrame({"ts": merged["ts"]})
        pred1 = pd.DataFrame({"ts": merged["ts"]})
        for col in target_columns:
            pred0[f"actual_{col}"] = merged[f"actual_{col}_m0"]
            pred0[f"pred_{col}"] = merged[f"pred_{col}_m0"]
            pred1[f"actual_{col}"] = merged[f"actual_{col}_m1"]
            pred1[f"pred_{col}"] = merged[f"pred_{col}_m1"]
    else:
        pred0 = pred_m0
        pred1 = pred_m1

    loss0 = _extract_loss_streams(pred0, target_columns)
    loss1 = _extract_loss_streams(pred1, target_columns)

    rng = np.random.default_rng(int(seed))
    out: dict[str, float] = {}
    keys = list(target_columns) + ["agg"]
    for k in keys:
        dm = dm_test(loss0[k], loss1[k], hac_lags=hac_lags, alternative="two-sided")
        d = (loss0[k] - loss1[k]).astype(float)
        mean_d, lo, hi = moving_block_bootstrap_mean_ci(
            d,
            block_size=int(block_size),
            n_boot=int(n_boot),
            alpha=0.05,
            rng=rng,
        )
        suffix = "agg" if k == "agg" else k
        out[f"dm_stat_{suffix}"] = float(dm.dm_stat)
        out[f"dm_p_{suffix}"] = float(dm.p_value)
        out[f"diff_mean_{suffix}"] = float(mean_d)
        out[f"diff_ci_lo_{suffix}"] = float(lo)
        out[f"diff_ci_hi_{suffix}"] = float(hi)
        out[f"dm_n_{suffix}"] = float(dm.n)
        out[f"dm_hac_lags_{suffix}"] = float(dm.hac_lags)
    return out


def _resolve_feature_list(
    feature_set: str,
    data_path: str,
    features_path: str,
    project_root: str,
) -> list[str]:
    """Resolve feature list depending on --feature-set."""
    baselines_dir = os.path.join(project_root, "baselines")
    if baselines_dir not in sys.path:
        sys.path.insert(0, baselines_dir)
    from utils import get_feature_columns, load_dataset  # type: ignore

    df = load_dataset(data_path)
    if feature_set == "full":
        return list(get_feature_columns(df))

    from transformer.dataset import load_recommended_features, resolve_features
    # Symbol-specific recommended_features.csv may be missing; fall back to global output.
    fp = features_path
    if fp and not os.path.exists(fp):
        alt = os.path.join(project_root, "feature_selection", "output", "recommended_features.csv")
        if os.path.exists(alt):
            fp = alt
    rec = load_recommended_features(fp)
    return list(resolve_features(df, rec))


